<h1> Information </h1>

<h4>Välkommen till QandA! </h4>
QandA är ett forum som har fokus på alla frågor och tips om kameror och objektiv.
Kan också förekomma frågor om bästa foto-platsen eller bara anmälla frågor om foto-livet.
Man måste ha en användare och vara inloggad för att kunna ställa en fråga samt svara och kommentera en fråga.
Men läsa i forumet kan alla göra!

<p>Poängsystem för att kunna avgöra vilken användare som är mest aktiv. Användaren får poäng genom:</p>
<ul>
 <li>1 poäng när de skapar en användare</li>
 <li>1 poäng för en fråga</li>
 <li>1 poäng för ett svar/kommentar</li>
</ul>

<p>Poängsystem för att kunna avgöra vilken tagg som används mest och är mest populär. Taggen får poäng genom:</p>
<ul>
 <li>1 poäng för när man skapar en tagg</li>
 <li>1 poäng när man använder taggen</li>
</ul>

